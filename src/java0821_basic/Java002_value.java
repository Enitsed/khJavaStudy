public class Java002_value {

	public static void main(String[] args) { // 프로그램 시작
		System.out.println(3); // 세미 콜론으로 문장의 끝을 표시한다.
		System.out.println(3);

		int num = 10; // 변수 선언
		System.out.println(num);
		System.out.println(num);
	} // end main() 프로그램 끝

}// end class
